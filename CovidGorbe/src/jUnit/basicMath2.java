public class basicMath2 {
    /**
     * subtracts 2 values
     * 
     * @param a a number
     * @param b a number
     * @return the subtraction of 2 numbers
     */
    public double kivonas(double a, double b){
        return a - b;
    }

    /**
     * divides 2 values
     * 
     * @param a a number
     * @param b a number
     * @return the division of the 2 numbers
     */
    public double osztas(double a, double b){
        return a / b;
    }

    /**
     * Returns the squared value
     * 
     * @param a a number
     * @return the squared value
     */
    public double square(double a){
        return a*a;
    }
}
