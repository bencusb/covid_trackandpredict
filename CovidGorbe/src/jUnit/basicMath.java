public class basicMath {

    /**
     * Sums 2 values
     * 
     * @param a a number
     * @param b a number
     * @return the sum of 2 numbers
     */
    public double osszead(double a, double b){
        return a + b;
    }

    /**
     * Multiplies 2 values
     * 
     * @param a a number
     * @param b a number
     * @return the multiplicate of the 2 numbers
     */
    public double osszeszoroz(double a, double b){
        return a * b;
    }

    /**
     * Returns the absolute value
     * 
     * @param a a number
     * @return the absolute value
     */
    public double abszolutertek(double a){
        return Math.abs(a);
    }
}
